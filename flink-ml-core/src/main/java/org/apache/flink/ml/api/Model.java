package org.apache.flink.ml.api;

import org.apache.flink.annotation.PublicEvolving;
import org.apache.flink.table.api.Table;

/**
 * A Model is typically generated by invoking {@link Estimator#fit(Table...)}. A Model is a
 * Transformer with the extra APIs to set and get model data.
 *
 * @param <T> The class type of the Model implementation itself.
 */
@PublicEvolving
public interface Model<T extends Model<T>> extends Transformer<T> {
    /**
     * Sets model data using the given list of tables. Each table could be an unbounded stream of
     * model data changes.
     *
     * @param inputs a list of tables
     */
    default T setModelData(Table... inputs) {
        throw new UnsupportedOperationException("this operation is not supported");
    }

    /**
     * Gets a list of tables representing the model data. Each table could be an unbounded stream of
     * model data changes.
     *
     * @return a list of tables
     */
    default Table[] getModelData() {
        throw new UnsupportedOperationException("this operation is not supported");
    }
}
